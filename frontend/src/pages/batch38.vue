})

// const task = ref('')
const todos = ref([
  {
    id: Date.now(),
    desc: 'Add Function',
    isDone: false
  }
])

function add () {
  todos.value.unshift({
    id: Date.now(),
    desc: state.task,
    isDone: false
  })
}
